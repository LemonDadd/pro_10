// 该文件已迁移至 js/ 目录的 ES Modules 系统
// 入口文件: js/main.js
// 模块划分说明:
//   - state.js: 全局状态 (state/editors/prettierPlugins)
//   - languages.js: 语言配置
//   - scriptManifest.js: 脚本清单 (本地路径+CDN fallback)
//   - libRegistry.js: 脚本加载注册表
//   - loadScripts.js: 动态脚本加载器 (本地→CDN fallback)
//   - ui.js: 状态栏/Toast/统计/语言检测
//   - theme.js: 主题初始化与切换
//   - editors.js: CodeMirror 编辑器初始化/操作
//   - formatters.js: Prettier/SQL 格式化
//   - minifiers.js: 各语言压缩 (HTML/CSS/JS/JSON/XML/SQL/MD)
//   - autoprefixer.js: Autoprefixer 处理与 Diff 渲染
//   - utils.js: 实用工具 (颜色/单位/HTML转义/Babel/JSON/Base64)
//   - hashState.js: URL Hash 状态存取/语言视图切换
//   - bindEvents.js: 事件绑定 (编辑器/选项/工具/快捷键)
//   - onlineStatus.js: 网络/库可用性检测
//   - main.js: 启动入口 (初始化顺序编排)
